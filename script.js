document.addEventListener('DOMContentLoaded', function () {
            document.getElementById('btn-detalhes-1').addEventListener('click', function () {
                document.getElementById('card-inner-1').classList.toggle('flip');
            });

            document.addEventListener('click', function (event) {
                if (event.target !== document.getElementById('btn-detalhes-1') && event.target !== document.getElementById('card-inner-1')) {
                    if (document.getElementById('card-inner-1').classList.contains('flip')) {
                        document.getElementById('card-inner-1').classList.remove('flip');
                    }
                }
            });
        });

